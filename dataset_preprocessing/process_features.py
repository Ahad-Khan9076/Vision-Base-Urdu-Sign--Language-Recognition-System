import cv2
import numpy as np
import os
import pandas as pd
from tqdm import tqdm
import mediapipe as mp

mp_holistic = mp.solutions.holistic

DATASET_ROOT = "/home/ahad/Documents/fyp_dataset"
OUTPUT_WORKSPACE = "/home/ahad/Documents/fyp_pipeline_outputs"
ML_DATASET_DIR = os.path.join(OUTPUT_WORKSPACE, "fyp_processed_dataset")
LANDMARKS_SAVE_DIR = os.path.join(ML_DATASET_DIR, "normalized_landmarks")

def process_video_optimized(video_path, holistic_instance, target_frames=60):
    cap = cv2.VideoCapture(video_path)
    sequence_landmarks = []

    while cap.isOpened():
        success, frame = cap.read()
        if not success:
            break

        image_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        results = holistic_instance.process(image_rgb)
        frame_landmarks = []

        if results.pose_landmarks:
            pose = results.pose_landmarks.landmark
            mid_shoulder_x = (pose[11].x + pose[12].x) / 2
            mid_shoulder_y = (pose[11].y + pose[12].y) / 2

            shoulder_width = np.sqrt((pose[11].x - pose[12].x)**2 + (pose[11].y - pose[12].y)**2)
            if shoulder_width == 0:
                shoulder_width = 1.0

            for idx in [11, 12, 13, 14, 15, 16]:
                norm_x = (pose[idx].x - mid_shoulder_x) / shoulder_width
                norm_y = (pose[idx].y - mid_shoulder_y) / shoulder_width
                frame_landmarks.extend([norm_x, norm_y, pose[idx].z])
        else:
            frame_landmarks.extend([0.0] * (6 * 3))

        if results.left_hand_landmarks:
            for lm in results.left_hand_landmarks.landmark:
                frame_landmarks.extend([lm.x, lm.y, lm.z])
        else:
            frame_landmarks.extend([0.0] * (21 * 3))

        if results.right_hand_landmarks:
            for lm in results.right_hand_landmarks.landmark:
                frame_landmarks.extend([lm.x, lm.y, lm.z])
        else:
            frame_landmarks.extend([0.0] * (21 * 3))

        sequence_landmarks.append(frame_landmarks)

    cap.release()

    raw_frame_count = len(sequence_landmarks)
    if raw_frame_count == 0:
        return None

    sequence_landmarks = np.array(sequence_landmarks)
    current_indices = np.linspace(0, raw_frame_count - 1, num=raw_frame_count)
    target_indices = np.linspace(0, raw_frame_count - 1, num=target_frames)

    resampled_matrix = np.zeros((target_frames, sequence_landmarks.shape[1]))
    for k in range(sequence_landmarks.shape[1]):
        resampled_matrix[:, k] = np.interp(target_indices, current_indices, sequence_landmarks[:, k])

    return resampled_matrix

manifest_csv_path = os.path.join(ML_DATASET_DIR, "initial_scan_manifest.csv")

if not os.path.exists(manifest_csv_path):
    print(f"Initial scan manifest missing. Run scan_dataset.py first!")
else:
    df_manifest = pd.read_csv(manifest_csv_path)
    success_count = 0
    failed_count = 0

    print(f"Starting HIGH-SPEED keypoint engine for {len(df_manifest)} videos...")
    print(f"Storing feature files safely inside: {LANDMARKS_SAVE_DIR}\n")

    with mp_holistic.Holistic(static_image_mode=False, model_complexity=1,
                              refine_face_landmarks=False) as holistic:

        for idx, row in enumerate(tqdm(df_manifest.itertuples(), total=len(df_manifest))):
            try:
                if os.path.exists(row.landmark_output_path):
                    success_count += 1
                    continue

                npy_features = process_video_optimized(row.raw_video_path, holistic, target_frames=60)

                if npy_features is not None:
                    np.save(row.landmark_output_path, npy_features)
                    success_count += 1
                else:
                    failed_count += 1
            except Exception as e:
                failed_count += 1
                print(f"Error on index {idx}: {str(e)}")

    print(f"\nHigh-speed optimization run completed locally!")
    print(f"Total valid stored matrices: {success_count} files.")
    print(f"Total skipped files: {failed_count} files.")
