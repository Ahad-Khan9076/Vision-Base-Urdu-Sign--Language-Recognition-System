import os
import pandas as pd
from sklearn.model_selection import train_test_split

OUTPUT_WORKSPACE = "/home/ahad/Documents/fyp_pipeline_outputs"
ML_DATASET_DIR = os.path.join(OUTPUT_WORKSPACE, "fyp_processed_dataset")
MANIFEST_PATH = os.path.join(ML_DATASET_DIR, "initial_scan_manifest.csv")

if not os.path.exists(MANIFEST_PATH):
    print(f"Cannot find manifest at {MANIFEST_PATH}. Please run your scan script first!")
else:
    df = pd.read_csv(MANIFEST_PATH)

    df['file_exists'] = df['landmark_output_path'].apply(os.path.exists)
    df_clean = df[df['file_exists'] == True].copy()
    df_clean = df_clean.drop(columns=['file_exists'])

    print(f"Verified {len(df_clean)} matching landmark files ready for split allocation.")

    unique_sentences = sorted(df_clean['sentence_id'].unique())
    label_map = {sent_id: idx for idx, sent_id in enumerate(unique_sentences)}

    df_clean['label_idx'] = df_clean['sentence_id'].map(label_map)

    df_map = pd.DataFrame(list(label_map.items()), columns=['sentence_id', 'label_idx'])
    text_lookup = df_clean[['sentence_id', 'sentence_text']].drop_duplicates()
    df_map = pd.merge(df_map, text_lookup, on='sentence_id')

    mapping_save_path = os.path.join(ML_DATASET_DIR, "sentence_mapping.csv")
    df_map.to_csv(mapping_save_path, index=False)
    print(f"Class label mapping key saved to: {mapping_save_path}")

    train_df, val_df = train_test_split(
        df_clean,
        test_size=0.20,
        stratify=df_clean['label_idx'],
        random_state=42
    )

    train_df = train_df.copy()
    val_df = val_df.copy()
    train_df['split'] = 'train'
    val_df['split'] = 'val'

    master_index_df = pd.concat([train_df, val_df]).sort_values(by=['sentence_id']).reset_index(drop=True)

    master_index_path = os.path.join(ML_DATASET_DIR, "master_dataset_index.csv")
    master_index_df.to_csv(master_index_path, index=False)

    print("\nDataset Stratification Summary:")
    print(f"   Total Training Instances: {len(train_df)} videos")
    print(f"   Total Validation Instances: {len(val_df)} videos")
    print(f"Master Index file successfully compiled at:\n   └─> {master_index_path}")
